  `source_id` bigint DEFAULT NULL COMMENT '来源业务ID',
  `source_item_id` bigint DEFAULT NULL COMMENT '来源业务明细ID',
  `source_no` varchar(64) DEFAULT NULL COMMENT '来源单号',
  `user_id` bigint NOT NULL COMMENT '用户编号',
  `grant_count` int NOT NULL DEFAULT 0 COMMENT '本来源发放次数',
  `used_count` int NOT NULL DEFAULT 0 COMMENT '本来源已用次数',
  `remain_count` int NOT NULL DEFAULT 0 COMMENT '本来源剩余次数',
  `status` tinyint NOT NULL DEFAULT 1 COMMENT '状态：1正常 2已退完 3失效',
  `grant_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '发放时间',
  `expire_time` datetime DEFAULT NULL COMMENT '来源失效时间',
  `remark` varchar(255) DEFAULT NULL COMMENT '备注',
  `creator` varchar(64) DEFAULT '' COMMENT '创建者',
  `createtime` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updater` varchar(64) DEFAULT '' COMMENT '更新者',
  `updatetime` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
